"""
Ejercicio 2.7¶
Escribe un programa que solicite tres números al usuario y calcule e imprima por pantalla su suma.
"""
numero1 = int(input("Escribe un número : "))
numero2 = int(input("Escribe otro número : "))
numero3 = int(input("Por último escribe otro más : "))

suma = numero1 + numero2 + numero3

print(f"{numero1} + {numero2} + {numero3} = {suma}")

